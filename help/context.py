def show_commands_table():
    """
    Prints all commands (General, APT, GPT) in a simple readable table format.
    """
    print("\n=== General Commands ===\n")
    print("ls       : List directory contents")
    print("cd       : Change directory")
    print("mkdir    : Create directory")
    print("rmdir    : Remove directory")
    print("touch    : Create empty file")
    print("cat      : Display file contents")
    print("rm       : Remove file")
    print("cp       : Copy file/directory")
    print("mv       : Move/rename file/directory")
    print("head     : Show first lines of file")
    print("tail     : Show last lines of file")
    print("tree     : Display directory tree")
    print("find     : Search files/directories")
    print("df       : Show disk usage of filesystems")
    print("du       : Show disk usage of files/directories")
    print("ps       : List running processes")
    print("top      : Show running processes interactively")
    print("kill     : Terminate process")
    print("shutdown : Shutdown the system")
    print("reboot   : Reboot the system")
    print("grep     : Search text in files")
    print("diff     : Compare files")

    print("\nFor full documentation, type: help -web\n")
